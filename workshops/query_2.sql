DECLARE @КонтейнерИД UNIQUEIDENTIFIER = '...'; -- SELECT TOP 1 ИД FROM Контейнеры;

SELECT
    '{' +
    '"ИД": "' + CAST(ИД AS NVARCHAR(50)) + '", ' +
    '"ИДКонтейнера": "' + CAST(ИДКонтейнера AS NVARCHAR(50)) + '", ' +
    '"ДатаНачалаОперации": "' + FORMAT(ДатаНачалаОперации, 'yyyy-MM-ddTHH:mm:ss') + '", ' +
    '"ДатаОкончанияОперации": "' + FORMAT(ДатаОкончанияОперации, 'yyyy-MM-ddTHH:mm:ss') + '", ' +
    '"ТипОперации": "' + ТипОперации + '", ' +
    '"ФИООператора": "' + ФИООператора + '", ' +
    '"МестоИнспекции": "' + МестоИнспекции + '"' +
    '}' AS JSONData
FROM Операции
WHERE ИДКонтейнера = @КонтейнерИД;
